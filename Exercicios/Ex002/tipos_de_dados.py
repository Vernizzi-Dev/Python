""" Número inteiro """
print(11 + 10);
""" Número de ponto flutuante """
print(1.5 + 0.5 + 1)
""" Tipo Booleano """
print(True);
""" String """
print("Python")
""" função dir() retorna todos os métodos e funções que posso usar dentro de um objeto """
nome = "Victor"
print(dir(nome))
""" função help() retorna o que é e o que faz, assim você consegue saber a funcionalidade de algo """
help(nome.upper)