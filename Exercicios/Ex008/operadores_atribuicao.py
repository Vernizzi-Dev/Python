""" ATRIBUIÇÃO SIMPLES: """
saldo_1 = 500;
print(saldo_1);

""" ATRIBUIÇÃO COM ADIÇÃO: """
saldo_2 = 500;
saldo_2 += 200;
print(saldo_2);

""" ATRIBUIÇÃO COM SUBTRAÇÃO: """
saldo_3 = 500;
saldo_3 -= 200;
print(saldo_3);

""" ATRIBUIÇÃO COM MULTIPLICAÇÃO: """
saldo_4 = 500;
saldo_4 *= 2;
print(saldo_4);

""" ATRIBUIÇÃO COM DIVISÃO: """
saldo_5 = 500;
saldo_5 /= 2;
print(saldo_5);

""" ATRIBUIÇÃO COM DIVISÃO INTEIRA: """
saldo_5 = 500;
saldo_5 //= 2;
print(saldo_5);

""" ATRIBUIÇÃO COM MÓDULO (resto da divisão): """
saldo_6 = 500;
saldo_6 %= 480;
print(saldo_6);

""" ATRIBUIÇÃO COM EXPONENCIAÇÃO: """
saldo_7 = 500;
saldo_7 **= 2;
print(saldo_7);